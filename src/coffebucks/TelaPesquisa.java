public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTableDesc.getModel();
        modelo.setNumRows(0);
        PedidoDAO pdao = new PedidoDAO();

        for (Pedido p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId_pedido(),
                p.getId_usuario(),
                p.getDescricao(),
                p.getQuantidade(),
                p.getPreco(),
                p.getData()
            });

        }

    }
public void readJTableForDesc(String desc) {
        
        DefaultTableModel modelo = (DefaultTableModel) jTableDesc.getModel();
        modelo.setNumRows(0);
        PedidoDAO pdao = new PedidoDAO();

        for (Pedido p : pdao.readForDesc(desc)) {

            modelo.addRow(new Object[]{
               p.getId_pedido(),
                p.getId_usuario(),
                p.getDescricao(),
                p.getQuantidade(),
                p.getPreco(),
                p.getData()
            });

        }

    }
