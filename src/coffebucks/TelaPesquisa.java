    private void jTableDescKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTableDescKeyReleased
           if (jTableDesc.getSelectedRow() != -1) {

            jTextFieldDesc.setText(jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 1).toString());
            jTextFieldQuant.setText(jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 2).toString());
            jTextFieldPreco.setText(jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 3).toString());

        }
    }//GEN-LAST:event_jTableDescKeyReleased

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
       if (jTableDesc.getSelectedRow() != -1) {
        Pedido p = new Pedido();
        PedidoDAO dao = new PedidoDAO();

        p.setDescricao(jTextFieldDesc.getText());
        p.setQuantidade(Integer.parseInt(jTextFieldQuant.getText()));
        p.setPreco(Double.parseDouble(jTextFieldPreco.getText()));
        p.setId_pedido((int)jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 0));
        p.setId_usuario((int)jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 0));
        dao.update(p);
         jTextFieldDesc.setText("");
            jTextFieldQuant.setText("");
            jTextFieldPreco.setText("");
        readJTable();
       }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
      if (jTableDesc.getSelectedRow() != -1) {

            Pedido p = new Pedido();
            PedidoDAO dao = new PedidoDAO();

            p.setId_pedido((int) jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 0));
             p.setId_usuario((int)jTableDesc.getValueAt(jTableDesc.getSelectedRow(), 1));
            dao.delete(p);

            jTextFieldDesc.setText("");
            jTextFieldQuant.setText("");
            jTextFieldPreco.setText("");

            readJTable();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed


